package EstructurasdeControl;

public class ejemplo5 {
//Ejercicio 5: Escribe un programa que imprima los números del 1 al
// 10, pero que se detenga cuando llegue al número 7 usando break.

    public static void main(String[] args) {

for (int i = 1 ; i <= 10; i++){
    if (i == 7){
        break;
    }
    System.out.println(i);
}
    }


}
